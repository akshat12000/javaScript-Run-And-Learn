// booleans & comparision operator(<,>,<=,>=)

// booleans
// true, false

let num1 = 5;
let num2 = 7;
console.log(num1>num2);

// == vs ===
let n1="7";
let n2=7;
console.log(n1==n2); // true -->data type doesn't matter until values are same!
console.log(n1===n2); //false

// != vs !==
console.log(num1!=num2); // true
console.log(n1!=n2); // false
console.log(n1!==n2); // true
